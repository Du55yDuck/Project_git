import random


# Функция для генерации случайных событий или ситуаций
def generated_event():
    events = [
        "Вы нашли ключи от закрытой двери.",
        "Вы услышали странный шум из соседней комнаты.",
        "Вы видите таинственную фигуру в темноте.",
        "Вам нужно выбрать, пойти направо или налево.",
        # Добавьте здесь другие события
    ]
    return random.choice(events)


# Основная функция игры
def play():
    print("Добро пожаловать в текстовую игру!")
    print("Вы проснулись в неизвестном месте.")

    while True:
        current_event = generated_event()
        print("\n" + current_event)

        choice = input("Что вы хотите сделать? (введите 'выход' для завершения игры): ")

        if choice.lower() == 'выход':
            print("Спасибо за игру! До свидания.")
            break

        # Добавьте здесь логику для обработки выборов игрока
        # Например, если игрок вводит "пойти налево", вы можете добавить соответствующую логику.
        # Важно также учесть возможные варианты исходов событий.
        else:
            print("Неизвестная команда. Попробуйте еще раз.")


if __name__ == "__main__":
    play()
